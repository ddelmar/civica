		<li>1. Roast the almonds at 375F for 10 minutes mixing them up halfway through.
		<li>2. In a medium sized saucepan; gently bring the honey, over medium heat, 
		<li>   to a slow boil. Once it starts to boil, turn the heat off, and set aside.
		<li>3. Whip the egg whites to a thick glossy meringue. Carefully, fold the meringue into the honey.
		<li>4. Once the meringue has been folded into the honey, bring the mixture back up to medium heat 
		<li>constantly stirring for 15-20 minutes while adding sugar. The meringue honey mixture will soon 
		<li>increase in volume.  As you keep stirring, it will condense itself until a thick caramel has formed. 
		<li>Also, the color will slowly change from a light brown to a deep burnt orange. The bottom of the mixture
		<li>may start to burn a bit which is okay. It adds depth in flavor. *To test if your mixture is thick enough,
		<li>add a bit to a plate and immediately place in the refrigerator.  After a minute or so, if it hardens, (your mixture is ready). 
		<li>5. Once the mixture has thickened add the roasted almonds and mix through.
		<li>Quickly pour the turron mixture into a parchment paper lined dish. Place another piece of parchment paper
		<li>directly on top of the mixture which will allow you to press it down and prevent anything from sticking to it.
		<li>6. Cool in the refrigerator for 2-3 hours before slicing.
